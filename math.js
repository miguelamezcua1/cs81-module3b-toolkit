// Doubles a number
function double(n) {
  return n * 2;
}

// Example test
console.log(double(4)); // Should print 8


// Returns the square of a number
function square(n) {
  return n * n;
}

// Example test
console.log(square(5)); // Should print 25


// Checks if a number is even
function isEven(n) {
  return n % 2 === 0;
}

// Example test
console.log(isEven(4)); // Should print true
console.log(isEven(7)); // Should print false


// Checks if a number is odd
function isOdd(n) {
  return n % 2 !== 0;
}

// Example test
console.log(isOdd(3)); // Should print true
console.log(isOdd(8)); // Should print false


// Multiplies two numbers
function multiply(a, b) {
  return a * b;
}

// Example test
console.log(multiply(3, 4)); // Should print 12
console.log(multiply(7, 0)); // Should print 0
